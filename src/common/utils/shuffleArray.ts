// https://decipher.dev/30-seconds-of-typescript/docs/shuffle/
export const shuffleArray = ([...arr]) => {
    let m = arr.length;
    while (m) {
        const i = Math.floor(Math.random() * m--);
        [arr[m], arr[i]] = [arr[i], arr[m]];
    }
    return arr;
};